function repairMojibake(value: string): string {
  let normalized = value;

  for (let i = 0; i < 3; i++) {
    if (!/[ÃÂâ€™â€œâ€žï]/.test(normalized)) break;
    try {
      const repaired = decodeURIComponent(escape(normalized));
      if (!repaired || repaired === normalized) break;
      normalized = repaired;
    } catch {
      break;
    }
  }

  return normalized;
}

const EN_TEXT_NORMALIZED: Record<string, string> = Object.fromEntries(
  Object.entries(EN_TEXT).map(([key, value]) => [repairMojibake(key), repairMojibake(value)]),
);
const SV_TEXT_NORMALIZED: Record<string, string> = Object.fromEntries(
  Object.entries(EN_TEXT_NORMALIZED).map(([key, value]) => [value, key]),
);

export function translateText(value: string, language: Language): string {
  const normalized = repairMojibake(value);
  if (language === 'sv') return SV_TEXT_NORMALIZED[normalized] ?? normalized;
  return EN_TEXT_NORMALIZED[normalized] ?? normalized;
}

export function localizeValue<T>(value: T, language: Language): T {
  if (typeof value === 'string') return translateText(value, language) as T;
  if (Array.isArray(value)) return value.map((item) => localizeValue(item, language)) as T;
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value as Record<string, unknown>).map(([key, item]) => [
        key,
        typeof item === 'function' ? item : localizeValue(item, language),
      ]),
    ) as T;
  }
  return value;
}
